const monographBlock = document.getElementById("monographBlock")
const monographImage = document.getElementById("monograph")
const monographText = document.getElementById("monographText")
monographBlock.addEventListener('mouseover', (event) =>{
  monographImage.classList.add('blur')
  monographText.classList.remove('hidden')
})
monographBlock.addEventListener('mouseleave', (event) =>{
  monographImage.classList.remove('blur')
  monographText.classList.add('hidden')
})

